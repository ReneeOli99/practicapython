nombre = "Renée" # Este tipo de variable es string es decir es una palabra, texto o frase
print(nombre) #Te muestra lo que pusiste como variable nombre
nombre = "Paola" #Cambiamos la variable nombre en lugar de ser Renée en este punto se convierte por Paola
print(nombre) #lo que hace es mostrar ahora la nueva variable paola

edad = 23 #este tipo de variable es entero lo cual significa que es un numero entero se usa este tipo de variables sin comillas para hacer operaciones
estatura = 1.53 #Este tipo de variable es Float lo cual significa que es un numero pero con numero decimal

print(edad+estatura) #Es suma
print(edad-estatura) #es una resta
print(edad*estatura) #Es una multiplicacion
print(edad/estatura) #Es una division
print(edad//estatura) #division sin puntos decimales
print(edad%estatura) #Modulo
print(edad**2) #forma de sacar una potencia